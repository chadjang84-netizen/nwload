class BandMapper:

    @staticmethod
    def earfcn_to_band(earfcn: int) -> int:
        for band, dl_low, dl_high, ul_low, ul_high in _LTE_BAND_TABLE:
            if dl_low <= earfcn <= dl_high:
                return band
            if ul_low is not None and ul_low <= earfcn <= ul_high:
                return band
        raise ValueError(f"EARFCN {earfcn} does not map to any known LTE band")

    @staticmethod
    def nrarfcn_to_band(nrarfcn: int) -> int:
        for band, low, high in _NR_BAND_TABLE:
            if low <= nrarfcn <= high:
                return band
        raise ValueError(f"NR_ARFCN {nrarfcn} does not map to any known NR band")

    @staticmethod
    def arfcn_to_band(arfcn: int, rat_type: RATType) -> int:
        if rat_type == RATType.LTE_PRIMARY:
            return BandMapper.earfcn_to_band(arfcn)
        elif rat_type in (RATType.NR, RATType.REDCAP):
            return BandMapper.nrarfcn_to_band(arfcn)
        raise ValueError(f"Cannot determine band for RAT_Type {rat_type}")
